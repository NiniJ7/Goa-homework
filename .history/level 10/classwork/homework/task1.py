# გააკეთეთ 5-5 მაგალითი თთოეულ შედარების ოპერატორზე (>, >=, <, <=, ==, !=), გვერდზე კომენტარის საშვალებით მიუთითეთ თუ რომელ boolean მნიშვნელობას გამოიტანას, აიღეთ მრავალფეროვანი კომბინაციები, შეეცადეთ გქონდეთ ყველა ვარიანტი

# (>)
print(5 > 2) # True
print(1 > 3) # False
print(9 > 16) # False
print(14 > 11) # True
print(-12 > -4) # False

# (<)
print(5 < 18) # True
print(1 < -2) # False
print(45 < 16) # False
print(-22 < 11) # True
print(1 < -4) # False

# (==)
print(5 == 5) # True
print(47 == -47) # False
print(9 == 16) # False
print(0.5 == 1/2) # True
print(-12 == -4) # False

# (>=)
print(5 >= 2) # True
print(1 >= 3) # False
print(22 >= 87) # False
print(14 >= 14) # True
print(-12 >= -4) # False

#(<=)
print(77 <= 90) # True
print(12 <= 3) # False
print(9 <= 6) # False
print(14 <= 18) # True
print(1,5 <= 3/2) # True

# (!=)
print(22 != 90) # True
print(12 != 12) # False
print("HI" != "Hi") # False
print("Football" != 'soccer') # True
print(1,5 != 3/2) # False


